// Language management
class LanguageManager {
    constructor() {
        this.currentLang = this.detectLanguage();
        this.init();
    }

    detectLanguage() {
        const saved = localStorage.getItem('preferred-language');
        if (saved) return saved;
        
        const browserLang = navigator.language.substring(0, 2);
        return translations[browserLang] ? browserLang : 'ar';
    }

    init() {
        this.updateUI();
        this.bindEvents();
    }

    bindEvents() {
        const languageSwitcher = document.getElementById('language-switcher');
        if (languageSwitcher) {
            languageSwitcher.addEventListener('click', () => this.toggleLanguage());
        }
    }

    toggleLanguage() {
        const newLang = this.currentLang === 'ar' ? 'en' : 'ar';
        this.setLanguage(newLang);
    }

    setLanguage(lang) {
        if (!translations[lang]) return;
        
        this.currentLang = lang;
        localStorage.setItem('preferred-language', lang);
        
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        
        this.updateUI();
    }

    updateUI() {
        // Update all elements with data-i18n attribute
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);
            if (translation) {
                element.textContent = translation;
            }
        });
        
        // Update language switcher text
        const switcher = document.getElementById('language-switcher');
        if (switcher) {
            const langAr = switcher.querySelector('.lang-ar');
            const langEn = switcher.querySelector('.lang-en');
            if (langAr && langEn) {
                langAr.style.display = this.currentLang === 'ar' ? 'none' : 'inline';
                langEn.style.display = this.currentLang === 'en' ? 'none' : 'inline';
            }
        }
    }

    t(key) {
        return translations[this.currentLang]?.[key] || translations['en'][key] || key;
    }
}

// Initialize language manager
window.languageManager = new LanguageManager();
